#include "libft.h"

int	ft_fs(int fd, char *p)
{
	int	i;

	if (p)
	{
		i = 0;
		while (p[i] != 0)
		{
			write(fd, &p[i], sizeof(char));
			++i;
		}
	}
	else
	{
		write(fd, "(null)", 6);
		i = 6;
	}
	return (i);
}
